/**
Classe utilizada para demonstrar o uso do operador Lógico E (& &)
**/

public class OperadorLogicoE {
	public static void main (String [] args) {
		boolean a = true;
		boolean b = false;
		boolean c = true;
		
		System.out.println(a && b);
		System.out.println(a && c);
	}
}

/**
O operador && é chamado de “operador E”. Ele retornará true se os dois operadores lógicos forem verdadeiros, logo, <operador1> && <operador2>
**/